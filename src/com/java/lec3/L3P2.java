/*
 * switch statement
 */
package com.java.lec3;

public class L3P2 {
    public static void main(String[] args) {
        
        int i = 10;
        
        switch (i) {
            case 5:
                System.out.println("i value is 5");
                break;
            case 10:
                System.out.println("i value is 10");
                break;
            case 15:
                System.out.println("i value is 20");
                break;
            default:
                System.out.println("this is default statement");
        }
        
        
    }
}
