public class L1P2 {
    public static void main(String[] args) {
        byte myb = 3;
        System.out.println("byte variable:"+myb);
        
        short mys = 10;
        System.out.println("short variable:"+mys);
        
        int myi = 33;
        System.out.println("int variable:"+myi);
        
        long myl = 1500L;
        System.out.println("long variable:"+myl);
        
        float myf = 355.25f;
        System.out.println("float variable:"+myf);
        
        double myd = 3500.22;
        System.out.println("double variable:"+myd);
        
        boolean mybool = true;
        System.out.println("boolean variable:"+mybool);
        
        char myc = 'm';
        System.out.println("char variable:"+myc);
        
    }
}
